import re
import numpy as np
import pandas as pd


def am_spectrum_xls(path0, path1):
    # 获取辐照数据列表
    with open(path0, 'r') as f:
        data = f.read()
    data = re.search('Direct\+circumsolar W\*m-2\*nm-1(.*)-2\*nm-1', data,
                     flags=re.DOTALL)
    data0 = data.group(1)
    data_all = re.sub('ASTM.*AM1.5\)', '', data0, flags=re.DOTALL)
    data_list = [i for i in data_all.split('\n') if i]
    data_list = [float(re.search('(\d\.\d*E[+-]\d{2})$', d).group(1)) for d in
                 data_list]

    # 获取波长列表（单位：um）
    n1 = list(np.arange(start=280, stop=400, step=0.5))
    n2 = list(range(400, 1700))
    n3 = [1700, 1702]
    n4 = list(range(1705, 4005, 5))
    waves = [i / 1000 for i in n1 + n2 + n3 + n4]

    # 获取波长-辐照矩阵
    am15 = pd.DataFrame(columns=['wave', 'DNI'])
    am15['wave'], am15['DNI'] = waves, data_list
    # print(waves)
    # print(data_list)

    # 输出EXCEL文件
    am_xls = pd.ExcelWriter(path1)
    am15.to_excel(am_xls, index=False)
    am_xls.close()

    print('suc')


